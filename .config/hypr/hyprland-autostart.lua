    -- Do not start waybar as systemd unit. hyprlshutdown will not work properly.
    -- All others, try to start as systemd service to enable auto restart.
    hl.exec_cmd("waybar")
    hl.exec_cmd("systemctl --user start app-blueman@autostart.service")
